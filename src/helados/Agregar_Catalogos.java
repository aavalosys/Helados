    //Este metodo nos permite mostrar las Marcas que Tenemos en la BD, en nuestro ComboBox
    // llamado marcaProducto.
    public void Marca(){
        Catalogo buscar = new Catalogo();
             ResultSet rs = buscar.Buscar_Marca();
             
        try {
            while(rs.next()){
               int a=0;
               marcaProducto.insertItemAt(rs.getString("nombre_marca"),a);
               a++;
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Catalogos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
   
